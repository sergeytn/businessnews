                    <p>
                    Чтобы успешно противостоять конкурентам на рынке, недостаточно просто предоставлять качественный продукт, финансово доступный для целевой аудитории. Нужно постоянно развиваться, искать новые решения, предлагать более удобные, функциональные и современные товары. Ведь если этого не сделаешь ты – это сделает кто-то другой.
                    </p>

                    <p>
                    Чтобы не болтаться в хвосте, нужно постоянно искать инновационные идеи для бизнеса. Для их поиска существуют специальные методики, помогающие выявить области, в которых «прячутся» возможности для инноваций. Итак, как же выглядят эти источники идей?
                    Потребности рынка
                    </p>

                    <p>
                    Как показывает статистика, три четверти инноваций на рынке рождаются именно благодаря спросу. В определенный момент возникает необходимость решения простых житейских проблем с помощью приборов, гаджетов или средств. Эта потребность некоторое время «витает в воздухе», прежде чем найдется изобретатель, способный предложить удобное решение. Оглянитесь вокруг себя – почти все, что вы видите в своей квартире – это ответы на запросы потребителя. Все бытовые приборы призваны облегчать нашу жизнь – быстро приготовить еду, постирать, охладить продукты, убрать в доме.
                    </p>

                    <p>
                    Кроме того, производители и сами являются потребителями, ожидающими разработки новой аппаратуры, с помощью которой можно оптимизировать производство.
                    </p>

                    <p>
                    Лучший источник инноваций – это покупатель. Не зря существует обратная связь, отзывы, опросники, рейтинги. Производитель должен четко понимать, что нужно людям, как можно усовершенствовать уже существующие товары, какие внедрить новинки.
                    </p>

                    <p>
                    Прекрасно, когда разработчики наименований не отрываются от народа, а сами пользуются собственной продукцией или хотя бы напрямую общаются с покупателями. Ведь очень часто бывает так, что производителю сложно увидеть недостатки своего творения и подумать о его доработке лишь потому, что он слишком далек от собственной целевой аудитории.
                    </p>

                    <h2>
                    Неожиданности
                    </h2>

                    <p>
                    Любое непредвиденное событие – это повод для размышления, ведь все в нашем мире не случайно. Каждое событие происходит благодаря определенным предпосылкам и является индикатором изменений в окружающем мире, обществе и, в частности, на рынке.
                    </p>

                    <p>
                    Этим событием может стать новая реакция потребителя на товар (стали меньше приобретать или, наоборот, внезапно увеличились объемы продаж без усилий со стороны производителя), сдвиг интересов целевой аудитории (товар, который покупали люди старшего возраста, вдруг стали приобретать дети).
                    </p>

                    <p>
                    Большую ошибку делают бизнесмены, воспринимающие подобные ситуации как нечто само собой разумеющееся. Непредвиденные результаты собственной работы нужно скрупулезно изучить, дабы понять причину происходящего и продумать дальнейшую стратегию компании.
                    </p>

                    <p>
                    Поиск инновационных идейНаиболее перспективными для разработки являются результаты неожиданного успеха. На их основе можно внедрить инновации, наименее подверженные риску по сравнению с другими экспериментальными проектами.
                    Расхождения между реальностью и представлениями о ней
                    </p>

                    <p>
                    ­Нередки ситуации, когда представления производителя о запросах и потребностях целевой аудитории кардинально расходятся с реальным положением дел.
                    </p>

                    <p>
                    Этим грешат не только новички, но и успешные компании. Потребителя может беспокоить проблема, встречающаяся в товарах одного типа у всех компаний — неудобство упаковки или сложности в использовании продукта. Покупатель молчит, поскольку думает, что производитель сам понимает проблему. А производитель уверен, что его продукт покупают, потому что никаких проблем нет.
                    </p>

                    <p>
                    Но чтобы резко обогнать конкурентов, достаточно выявить и устранить эту недоработку. Причем иногда она кроется в мелочах, до смешного очевидных.
                    </p>

                    <p>
                    Приведу маленький пример из повседневной жизни домохозяек: практически все емкости для хранения вилок и ложек имеют форму стакана, закрытого снизу, из-за чего в нем застаивается вода. Первый, кто придумал производить емкость со сливом, запатентовал ее как уникальное изобретение и продает эти нехитрые приспособления за большие деньги. А ведь проблема существует давно.
                    </p>

                    <p>
                    Такие же «прорехи» можно найти и в экономических графиках, и в производственных показателях. Вероятно, производитель бросает слишком много усилий на решение задач, не приносящих ожидаемого эффекта, или рост продаж не влечет за собой адекватного роста прибыли? Если имеются подобные расхождения – значит, нужно найти проблему, решить ее, и это поможет выйти на новый уровень.

                    </p>

                    <H2>Демографический фактор</H2>

                    <p>
                    Любая компания работает для потребителя, поэтому исследование демографических изменений играет важную роль. Методы поиска инновационных идей включают в себя плотную работу по изучению состава населения. Соотношение количества мужчин и женщин, молодых и пожилых людей, уровень доходов, религиозные и политические взгляды – все это формирует запросы целевой аудитории. Каждое изменение влечет за собой количественные и качественные сдвиги в показателях рынка.

                    </p>

                    <H2>Изменения в обществе, взглядах людей</H2>

                    <p>
                    методы поиска инновационных идейМножество ответов на вопрос, как найти инновационную идею, кроются в самом обществе, традициях, мировоззрении и стереотипах.
                    Не столь давно, в 2000-х годах, производители женской одежды считали, что красивые и модные вещи носят только молоденькие девочки миниатюрных размеров. Для женщин и девушек крупного телосложения отшивались довольно невзрачные, а порой и откровенно уродливые вещи – будто бы женщина после тридцати теряла право быть современной и модной. Поход на рынок стал настоящей катастрофой для девушек с пышными формами.
                    </p>

                    <p>
                    К счастью для женщин, в начале десятых годов этого века были разрушены сразу два стереотипа: что молодая девушка обязательно худенькая, и что полная женщина средних лет априори некрасива.
                    </p>

                    <p>
                    Появилось множество фотосессий с храбрыми «пышками», модельеры обратили свой взор на моделей XXXL, и производители вдруг поняли – большие женщины тоже хотят красиво одеваться и имеют на это полное право.
                    </p>

                    <p>
                    Эти преобразования не были просто модной тенденцией. Копнув глубже, мы поймем, что все дело в изменениях семейных ценностей запада, имеющего серьезное культурное влияние на славянские страны. Для женщины, получившей возможность обеспечивать себя и строить карьеру, замужество перестало быть средством для выживания. Теперь она не торопится под венец. В Европе и США вполне нормально, когда впервые выходят замуж «девушки» в 30 или 40 лет (а к этому возрасту многие дамы становятся немножко шире, так уж заложено природой). А значит, желание быть яркой и привлекательной может длиться дольше, чем до 20 лет. Кроме того, исчез стереотип о том, что замужняя женщина должна быть похожа на серую мышь. Так появились специализированные магазины с модной одеждой больших размеров, а чуть позже яркие наряды для толстушек перекочевали на рыночные ряды. К слову, производители на этом еще и неплохо заработали, ведь женщины среднего возраста, желающие красиво одеваться – это чаще всего бизнес-леди или представительницы руководящего звена.Так изменение представлений о браке повлияло на моду.
                    </p>

                    <h2>

                    Новые знания, изобретения
                    </h2>

                    <p>
                    Где взять инновационные идеи для бизнеса? В науке! Новые знания имеют в первую очередь прикладную цель. Совершив открытие в любой области, его «родитель» чаще всего думает, какую практическую пользу оно может принести человечеству (и за сколько его можно продать).
                    </p>

                    <p>
                    Новые знания и изобретения меняли жизнь людей с самого начала – начиная с приручения огня и изобретения колеса, и заканчивая приложениями на смартфон, с помощью которых можно мерять давление или отслеживать перемещение детей.
                    </p>

                    <p>
                    Как искать инновационные идеиЗнания можно монетизировать, но для этого необходимо постоянно следить за научной деятельностью, связанной с вашим производством. И даже если завод выпускает марлю, тазики, кастрюли или любые другие простые товары — грешно думать, что науке здесь нечего делать. Возьмем для примера тефлон – это широко известный продукт химической промышленности, использующийся для изготовления многих товаров: одежды, ламп, кипятильников, бритвенных лезвий, унитазов. Но знаем мы о нем в первую очередь благодаря посуде с тефлоновым покрытием. Не будем говорить о его пользе для здоровья, но благодаря антипригарным свойствам такая посуда в определенный момент стала ведущим товаром на рынке.
                    Необходимость производственных инноваций
                    </p>

                    <p>
                    Известно, что сделать товар более доступным помогает удешевление производства. Но не за счет менее качественного сырья (хотя многие именно этим и занимаются), а через поиск технических решений, которые облегчат процессы.
                    </p>

                    <p>
                    Этими решениями может стать и приобретение более мощной аппаратуры, с помощью которой можно производить товары лучшего качества и в более короткие сроки, и освоение тех услуг, за которые приходилось платить другим исполнителям. Так, наиболее выгодно производить товар предприятиям, где все процессы происходят внутри компании – начиная от самостоятельного производства сырья и заканчивая упаковкой и развозкой товара.
                    </p>

                    <p>
                    История знает примеры технологических инноваций, перевернувших с ног на голову целые отрасли – от изобретения станков, давших начало промышленной революции, до цифровых методов обработки информации, применяющихся сегодня во всех производственных сферах.
                    </p>

                    <p>
                    Что ж, давайте сделаем вывод: поиск инновационных идей в современном бизнесе – это необходимость, ведь и промышленность, и сфера услуг развиваются огромными темпами, а слабые конкуренты безжалостно выдавливаются с рынка. Для того, чтобы оставаться «в тренде», нужно беспрерывно изучать ситуацию и не упускать возможности развития.</p>


